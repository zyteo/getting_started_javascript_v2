// Operations

// binary operators (+ and -)
// operands are on the left and right of the operator
// example: 3 is the left operand, 4 is the right operand, and + is the operator
3 + 4
43 - 1

// Adding string
"ZY" + "Teo"

// Unary operators
// only has one operand
// ! takes the negation of a boolean
!false

// loose equality vs strict equality
// loose equality uses == and strict equality uses ===
// in both cases, true
3.0 == 3
3.0 === 3

// binary operator as well
3 < 4

// && and || are logical operators
// && is logical AND
// || is logical OR
true || false
true && false