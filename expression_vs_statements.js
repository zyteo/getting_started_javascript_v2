number = 1 + (number * 2);
// for the above case, there are 6 expressions and 1 statement
// 2 is an expression
// number is an expression
// (number * 2) is an expression
// 1 is an expression
// 1 + (number * 2) is an expression
// number = 1 + (number * 2) is an expression

// all 6 of the above expressions are part of a single statement
